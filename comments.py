#Single-Line Comments:
#Single-line comments are used to comment on a single line of code. They start with the # symbol and continue until the end of the line.
# This is a single-line comment
print("Hello, World!")  # This is also a comment after code


#Multi-Line Comments:
#Python doesn't have a built-in syntax specifically for multi-line comments like some other programming languages. However, you can use multi-line strings (triple-quoted strings) as a way to achieve multi-line comments. Although these strings are not technically comments, they serve the same purpose and are often used as a workaround



"""
This is a multi-line comment.
You can write as many lines as you want within these triple quotes.
"""
print("Python is great!")

